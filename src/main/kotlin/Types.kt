class Types {
    fun exploreTypes() {
        var int: Int = 777
        var byte: Byte = 1
        var short: Short = 20
        var long: Long = 7000000000
        var next = 200L
        var float: Float = 77.77f
        var double: Double = 77.77

        var boolean: Boolean = true
        var booleanFalse = false

        var char: Char = 'a'
        var string: String = "Hello"


        println(int)
        println(byte)
        println(short)
        println(long)
        println(next)
        println(float)
        println(double)

        println(boolean)
        println(booleanFalse)

        println(char)
        println(string)
    }
}